package ejercicio1;

public class Punto {
	public int x;
	public int y;
	
	// constructor
	public Punto(int x, int y) {
		this.x = x;
		this.y = y;
	}
	
	// gets y sets
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
}
